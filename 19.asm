.MODEL SMALL
.STACK 100H

.DATA

  NUM DB ?
  MSG1 DB 10,13,'ENTER NO: $' 
  MSG2 DB 10,13,'NOT PRIME: $' 
  MSG3 DB 10,13,'PRIME $' 

.CODE
MAIN PROC

  MOV AX,@DATA
  MOV DS,AX

  LEA DX,MSG1
  MOV AH,9
  INT 21H

  MOV AH,1
  INT 21H
  SUB AL,30H
  MOV NUM,AL

  CMP AL,1
  JLE LBL2
  MOV AH,00
  CMP AL,3
  JLE LBL3
  MOV AH,00

  MOV CL,2
  DIV CL
  MOV CL,AL
  
LBL1:

  MOV AH,00
  MOV AL,NUM
  DIV CL
  CMP AH,00
  JZ LBL2
  DEC CL
  CMP CL,1
  JNE LBL1
  JMP LBL3

LBL2:

  MOV AH,9
  LEA DX,MSG2
  INT 21H
  JMP EXIT

LBL3:

  MOV AH,9
  LEA DX,MSG3
  INT 21H

EXIT:

  MOV AH,4CH
  INT 21H 
  
   
MAIN ENDP
END MAIN 